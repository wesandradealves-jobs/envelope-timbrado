function selector(e){
    var e = jQuery(e),
        index = e.parent().index(),
        counter = parseInt(e.closest(".porque-slider-nav").attr("data-counter")),
        max = e.closest(".porque-slider-nav").next().children().length,
        i = 0;

        if(index == 1){
            if(counter < max){
                e.closest(".porque-slider-nav").attr("data-counter", (parseInt(e.closest(".porque-slider-nav").attr("data-counter")) + 1));
                e.closest(".porque-slider-nav").next().children().eq(counter).addClass("-active");
                if(counter > 0){
                    e.closest(".porque-slider-nav").next().children().eq(counter).prev().removeClass("-active"); 
                }
            }
        } else {
            if(counter > 0){
                e.closest(".porque-slider-nav").attr("data-counter", (parseInt(e.closest(".porque-slider-nav").attr("data-counter")) - 1));
                e.closest(".porque-slider-nav").next().children().eq(counter - 1).removeClass("-active");

                e.closest(".porque-slider-nav").next().children().eq(counter).removeClass("-active"); 
                if(counter > 1){
                    e.closest(".porque-slider-nav").next().children().eq(counter - 2).addClass("-active"); 
                }
            }
        }
}